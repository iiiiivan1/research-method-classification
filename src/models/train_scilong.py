def load_jsonl_dataset(train_path: Path, val_path: Path):
    """Load train and validation JSONL files and cast labels to float."""
    dataset = load_dataset(
        "json",
        data_files={
            "train": str(train_path),
            "validation": str(val_path),
        },
    )

    def cast_labels(batch):
        batch["labels"] = [[float(value) for value in y] for y in batch["labels"]]
        return batch

    dataset = dataset.map(cast_labels, batched=True, desc="Casting labels")

    return dataset


def list_pairs_from_dir(data_dir: Path) -> List[str]:
    """List all available pair IDs from a data directory."""
    pattern = re.compile(r"^train_(\d{2}-\d{2})\.jsonl$")
    pairs = []

    for path in data_dir.iterdir():
        if path.is_file():
            match = pattern.match(path.name)

            if match:
                pairs.append(match.group(1))

    pairs = sorted(pairs)

    if not pairs:
        raise FileNotFoundError(f"No train_??-??.jsonl files found in {data_dir}")

    return pairs


def filter_pairs(pairs: List[str]) -> List[str]:
    """Filter pair IDs according to PAIR_ID or START_PAIR / END_PAIR settings."""
    if PAIR_ID:
        if PAIR_ID not in pairs:
            raise FileNotFoundError(f"PAIR_ID={PAIR_ID} not found. Example pairs: {pairs[:5]}")
        return [PAIR_ID]

    if START_PAIR and END_PAIR:
        if START_PAIR not in pairs or END_PAIR not in pairs:
            raise FileNotFoundError(
                f"START_PAIR or END_PAIR not found. START_PAIR={START_PAIR}, END_PAIR={END_PAIR}"
            )

        start_idx = pairs.index(START_PAIR)
        end_idx = pairs.index(END_PAIR)

        if start_idx > end_idx:
            start_idx, end_idx = end_idx, start_idx

        return pairs[start_idx:end_idx + 1]

    return pairs


def train_one(pair_name: str) -> None:
    """Train SciBERT-long on one P4 segment pair."""
    train_file = DATA_DIR / f"train_{pair_name}.jsonl"
    val_file = DATA_DIR / f"val_{pair_name}.jsonl"

    if not train_file.exists() or not val_file.exists():
        print(f"[P4_{pair_name}] Missing train or validation JSONL file. Skipping.")
        return

    raw_dataset = load_jsonl_dataset(train_file, val_file)

    if len(raw_dataset["train"]) == 0:
        print(f"[P4_{pair_name}] Empty training set. Skipping.")
        return

    out_dir = OUT_ROOT / f"P4_{pair_name}"
    out_dir.mkdir(parents=True, exist_ok=True)

    device = "cuda" if torch.cuda.is_available() else "cpu"

    print(
        f"\n=== Training P4_{pair_name} | "
        f"device={device} | "
        f"train={len(raw_dataset['train'])} | "
        f"validation={len(raw_dataset['validation'])} ==="
    )
    print(
        f"TRUNC_MODE={TRUNC_MODE} | MAX_LEN={MAX_LEN} | "
        f"MAX_TA={MAX_TA} | MAX_I={MAX_I} | MAX_J={MAX_J}"
    )

    tokenizer = AutoTokenizer.from_pretrained(MODEL_NAME, use_fast=True)

    base_model = AutoModelForSequenceClassification.from_pretrained(
        MODEL_NAME,
        num_labels=NUM_LABELS,
        problem_type="multi_label_classification",
    )

    target_modules = pick_lora_targets(base_model)

    lora_config = LoraConfig(
        r=32,
        lora_alpha=64,
        lora_dropout=0.1,
        bias="none",
        task_type=TaskType.SEQ_CLS,
        target_modules=target_modules,
        modules_to_save=["classifier"],
    )

    model = get_peft_model(base_model, lora_config)

    if hasattr(model, "gradient_checkpointing_enable"):
        model.gradient_checkpointing_enable()

    model.config.use_cache = False
    model.enable_input_require_grads()

    remove_columns = [
        column for column in raw_dataset["train"].column_names
        if column != "labels"
    ]

    dataset = raw_dataset.map(
        lambda batch: preprocess(batch, tokenizer),
        batched=True,
        remove_columns=remove_columns,
        desc=f"Tokenizing P4_{pair_name}",
    )

    base_collator = DataCollatorWithPadding(
        tokenizer=tokenizer,
        pad_to_multiple_of=8,
    )

    def collator(features):
        labels = [feature.pop("labels") for feature in features]
        batch = base_collator(features)
        batch["labels"] = torch.tensor(labels, dtype=torch.float32)
        return batch

    training_args = TrainingArguments(
        output_dir=str(out_dir),
        seed=SEED,
        report_to="none",

        bf16=torch.cuda.is_available() and torch.cuda.is_bf16_supported(),
        fp16=False,

        # For older Transformers versions, replace eval_strategy with evaluation_strategy.
        eval_strategy="steps",
        eval_steps=EVAL_STEPS,
        save_strategy="steps",
        save_steps=SAVE_STEPS,
        save_total_limit=2,
        logging_steps=LOGGING_STEPS,

        learning_rate=LR,
        num_train_epochs=EPOCHS,
        per_device_train_batch_size=TRAIN_BS,
        per_device_eval_batch_size=EVAL_BS,
        gradient_accumulation_steps=GRAD_ACCUM,

        warmup_ratio=0.1,
        weight_decay=0.01,
        max_grad_norm=1.0,

        load_best_model_at_end=True,
        metric_for_best_model="eval_micro_f1",
        greater_is_better=True,

        disable_tqdm=False,
    )

    trainer = ASLTrainer(
        gamma_neg=GAMMA_NEG,
        gamma_pos=GAMMA_POS,
        clip=CLIP,
        model=model,
        args=training_args,
        train_dataset=dataset["train"],
        eval_dataset=dataset["validation"],
        data_collator=collator,
        compute_metrics=compute_metrics,
        callbacks=[EarlyStoppingCallback(early_stopping_patience=2)],
    )

    trainer.train()

    adapter_dir = out_dir / "adapter"
    adapter_dir.mkdir(parents=True, exist_ok=True)

    trainer.model.save_pretrained(str(adapter_dir))
    tokenizer.save_pretrained(str(adapter_dir))

    print(f"[P4_{pair_name}] Adapter saved to: {adapter_dir}")

    del trainer, model, base_model, tokenizer, dataset, raw_dataset
    gc.collect()

    if torch.cuda.is_available():
        torch.cuda.empty_cache()


def main() -> None:
    pairs = list_pairs_from_dir(DATA_DIR)
    run_pairs = filter_pairs(pairs)

    print(f"DATA_DIR: {DATA_DIR}")
    print(f"OUT_ROOT: {OUT_ROOT}")
    print(f"Found pairs: {len(pairs)}")
    print(
        "Will run pairs:",
        run_pairs[:10],
        "..." if len(run_pairs) > 10 else "",
    )

    for pair in run_pairs:
        train_one(pair)


if __name__ == "__main__":
    main()