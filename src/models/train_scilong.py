def budget_defaults(max_len: int):
    """
    Return default token budgets for P4 input.

    For max_len=4096, the default allocation is approximately:
        Title + Abstract: 1024 tokens
        Segment i: 1536 tokens
        Segment j: 1536 tokens
    """
    ta = min(1024, max_len // 4)
    remaining = max_len - ta
    i = remaining // 2
    j = remaining - i
    return ta, i, j


def preprocess(examples, tokenizer):
    """
    Tokenize P4 samples for SciBERT-long.

    Input structure:
        A = Title + Abstract + Segment i
        B = Segment j

    Global attention:
        CLS token and the A segment up to the first SEP token are assigned
        global attention.
    """
    titles = examples.get("title", [""] * len(examples["chunk_i"]))
    abstracts = examples.get("abstract", [""] * len(examples["chunk_i"]))
    chunk_is = examples.get("chunk_i", [""] * len(examples["chunk_i"]))
    chunk_js = examples.get("chunk_j", [""] * len(examples["chunk_i"]))

    if isinstance(titles, str):
        titles = [titles]
    if isinstance(abstracts, str):
        abstracts = [abstracts]
    if isinstance(chunk_is, str):
        chunk_is = [chunk_is]
    if isinstance(chunk_js, str):
        chunk_js = [chunk_js]

    input_ids_list = []
    attention_mask_list = []
    global_attention_mask_list = []

    default_ta, default_i, default_j = budget_defaults(MAX_LEN)
    ta_budget = MAX_TA if MAX_TA > 0 else default_ta
    i_budget = MAX_I if MAX_I > 0 else default_i
    j_budget = MAX_J if MAX_J > 0 else default_j

    sep_id = tokenizer.sep_token_id

    for title, abstract, chunk_i, chunk_j in zip(titles, abstracts, chunk_is, chunk_js):
        title = title or ""
        abstract = abstract or ""
        chunk_i = chunk_i or ""
        chunk_j = chunk_j or ""

        if TRUNC_MODE == "longest":
            a_text = title.strip()

            if abstract.strip():
                a_text = (
                    a_text + "\n\n" + abstract.strip()
                ).strip() if a_text else abstract.strip()

            if chunk_i.strip():
                a_text = (
                    a_text + "\n\n[Body Segment i]\n" + chunk_i.strip()
                ).strip() if a_text else chunk_i.strip()

            encoded = tokenizer(
                a_text,
                chunk_j.strip(),
                truncation=True,
                max_length=MAX_LEN,
                padding=False,
            )

        else:
            ta_text = title.strip()

            if abstract.strip():
                ta_text = (
                    ta_text + "\n\n" + abstract.strip()
                ).strip() if ta_text else abstract.strip()

            ta_ids = tokenizer(
                ta_text,
                truncation=True,
                max_length=ta_budget,
                padding=False,
            )["input_ids"]

            i_ids = tokenizer(
                chunk_i.strip(),
                truncation=True,
                max_length=i_budget,
                padding=False,
            )["input_ids"]

            j_ids = tokenizer(
                chunk_j.strip(),
                truncation=True,
                max_length=j_budget,
                padding=False,
            )["input_ids"]

            ta_text_cut = tokenizer.decode(ta_ids, skip_special_tokens=True).strip()
            i_text_cut = tokenizer.decode(i_ids, skip_special_tokens=True).strip()
            j_text_cut = tokenizer.decode(j_ids, skip_special_tokens=True).strip()

            a_text = ta_text_cut

            if i_text_cut:
                a_text = (
                    a_text + "\n\n[Body Segment i]\n" + i_text_cut
                ).strip() if a_text else i_text_cut

            encoded = tokenizer(
                a_text,
                j_text_cut,
                truncation=True,
                max_length=MAX_LEN,
                padding=False,
            )

        input_ids = encoded["input_ids"]
        attention_mask = encoded["attention_mask"]

        global_attention_mask = [0] * len(input_ids)

        if global_attention_mask:
            global_attention_mask[0] = 1

        if sep_id is not None:
            try:
                sep_index = input_ids.index(sep_id)
                for idx in range(sep_index + 1):
                    global_attention_mask[idx] = 1
            except ValueError:
                pass

        input_ids_list.append(input_ids)
        attention_mask_list.append(attention_mask)
        global_attention_mask_list.append(global_attention_mask)

    return {
        "input_ids": input_ids_list,
        "attention_mask": attention_mask_list,
        "global_attention_mask": global_attention_mask_list,
    }


def pick_lora_targets(model) -> List[str]:
    """
    Select LoRA target modules from the model.

    The function prefers attention-related modules and falls back to
    query/key/value when needed.
    """
    suffixes = set()

    for name, module in model.named_modules():
        if module.__class__.__name__ in ("Linear", "Conv1D"):
            suffixes.add(name.split(".")[-1])

    preferred = [
        "query",
        "key",
        "value",
        "query_global",
        "key_global",
        "value_global",
        "dense",
    ]

    selected = [module_name for module_name in preferred if module_name in suffixes]

    if not {"query", "key", "value"}.issubset(set(selected)):
        print("q/k/v modules were not fully detected. Falling back to query/key/value.")
        return ["query", "key", "value"]

    return selected


# =========================
# Asymmetric Loss
# =========================

class AsymmetricLoss(nn.Module):
    """
    Asymmetric Loss for multi-label classification.

    This loss down-weights easy negative labels and is useful for imbalanced
    multi-label classification tasks.
    """

    def __init__(self, gamma_neg=4.2, gamma_pos=1.0, clip=0.05, eps=1e-8):
        super().__init__()
        self.gamma_neg = gamma_neg
        self.gamma_pos = gamma_pos
        self.clip = clip
        self.eps = eps

    def forward(self, logits, targets):
        prob = torch.sigmoid(logits)

        if self.clip and self.clip > 0:
            prob = torch.clamp(prob, min=self.eps, max=1 - self.eps)
            prob_neg = torch.clamp(1 - prob + self.clip, max=1.0)
        else:
            prob_neg = 1 - prob

        prob_pos = prob

        loss_pos = targets * torch.log(prob_pos + self.eps)
        loss_neg = (1 - targets) * torch.log(prob_neg + self.eps)
        loss = loss_pos + loss_neg

        pt = prob_pos * targets + prob_neg * (1 - targets)
        gamma = self.gamma_pos * targets + self.gamma_neg * (1 - targets)
        focal_weight = torch.pow(1 - pt, gamma)

        return -(loss * focal_weight).mean()


class ASLTrainer(Trainer):
    """Trainer using Asymmetric Loss instead of the default BCE loss."""

    def __init__(self, gamma_neg=4.2, gamma_pos=1.0, clip=0.05, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self.asl = AsymmetricLoss(gamma_neg=gamma_neg, gamma_pos=gamma_pos, clip=clip)

    def compute_loss(self, model, inputs, return_outputs=False):
        labels = inputs.pop("labels")
        outputs = model(**inputs)
        loss = self.asl(outputs.logits, labels)
        return (loss, outputs) if return_outputs else loss


